"""Workspace alert file for scheduler job failures.

Writes imperative alerts to {working_directory}/.claude/scheduler-alerts.md
so that any agent (terminal or Telegram) sees them on session start via
the @.claude/scheduler-alerts.md include in CLAUDE.md.

Alert format is a direct briefing — not a log. A stateless agent with
no context beyond this file must be able to act on it.
"""

from datetime import UTC, datetime
from pathlib import Path
from typing import Any, Dict, List, Optional

import structlog

logger = structlog.get_logger()

ALERT_FILENAME = ".claude/scheduler-alerts.md"

# Header written at top of non-empty alert files
_FILE_HEADER = """\
<!-- AUTO-GENERATED by scheduler alert system. Do NOT edit manually. -->
# Scheduler Alerts

**These are active alerts from failed or missed scheduled jobs.**
**You MUST act on them. After resolving each alert, remove it from this file.**
**If all alerts are resolved, empty this file completely.**

Run `date` to check the current time before making decisions.

"""


def _format_alert(job: Dict[str, Any], reason: str) -> str:
    """Format a single job alert as an imperative agent briefing."""
    job_name = job.get("job_name", "unknown")
    job_id = job.get("job_id", "unknown")
    prompt = job.get("prompt", "")
    run_at = job.get("run_at", "unknown")
    priority = job.get("priority", "medium")
    on_failure = job.get("on_failure")
    relevance_hours = job.get("relevance_hours")
    attempts = job.get("attempts", 0)
    last_error = job.get("last_error", "")
    now = datetime.now(UTC).isoformat()

    lines = [
        f"## ALERT: {job_name}",
        f"**Priority**: {priority.upper()}",
        f"**Job ID**: `{job_id}`",
        f"**What failed**: {reason}",
        f"**Scheduled for**: {run_at}",
        f"**Alert written at**: {now}",
        f"**Attempts so far**: {attempts}",
    ]

    if last_error:
        lines.append(f"**Last error**: {last_error}")

    if relevance_hours:
        lines.append(
            f"**Relevance window**: {relevance_hours} hours after scheduled time. "
            "If this window has passed, this alert is no longer actionable — "
            "clear it and move on."
        )

    lines.append("")
    lines.append("**Original job prompt**:")
    # Truncate very long prompts
    prompt_display = prompt[:500] + "..." if len(prompt) > 500 else prompt
    lines.append(f"> {prompt_display}")
    lines.append("")

    if on_failure:
        lines.append("**Recovery instructions** (from job creator):")
        lines.append(on_failure)
        lines.append("")
    else:
        lines.append(
            "**No recovery instructions were provided.** "
            "Raise this to the user and ask what to do."
        )
        lines.append("")

    lines.append("---")
    lines.append("")
    return "\n".join(lines)


def write_alert(
    working_directory: Path,
    job: Dict[str, Any],
    reason: str,
) -> None:
    """Append an alert to the workspace alert file.

    Creates the .claude directory and file if they don't exist.
    Preserves existing alerts (appends).
    """
    alert_path = working_directory / ALERT_FILENAME

    try:
        alert_path.parent.mkdir(parents=True, exist_ok=True)

        existing = ""
        if alert_path.exists():
            existing = alert_path.read_text(encoding="utf-8")

        alert_block = _format_alert(job, reason)

        if not existing.strip():
            # Fresh file — write header + alert
            content = _FILE_HEADER + alert_block
        else:
            # Append to existing alerts
            content = existing.rstrip("\n") + "\n\n" + alert_block

        alert_path.write_text(content, encoding="utf-8")

        logger.info(
            "scheduler.alert_written",
            job_id=job.get("job_id"),
            job_name=job.get("job_name"),
            working_directory=str(working_directory),
            reason=reason,
        )
    except Exception:
        logger.exception(
            "scheduler.alert_write_failed",
            job_id=job.get("job_id"),
            working_directory=str(working_directory),
        )


def clear_alert(working_directory: Path, job_id: str) -> bool:
    """Remove a specific alert from the workspace alert file.

    Returns True if the alert was found and removed.
    """
    alert_path = working_directory / ALERT_FILENAME

    if not alert_path.exists():
        return False

    try:
        content = alert_path.read_text(encoding="utf-8")

        # Split into alert blocks and filter out the matching job_id
        # Each alert starts with "## ALERT:" and ends before the next one
        sections: List[str] = []
        current_section: List[str] = []
        found = False

        for line in content.split("\n"):
            if line.startswith("## ALERT:"):
                if current_section:
                    section_text = "\n".join(current_section)
                    if f"`{job_id}`" in section_text:
                        found = True
                    else:
                        sections.append(section_text)
                current_section = [line]
            else:
                current_section.append(line)

        # Don't forget the last section
        if current_section:
            section_text = "\n".join(current_section)
            if f"`{job_id}`" in section_text:
                found = True
            else:
                sections.append(section_text)

        if not found:
            return False

        # Rebuild the file
        remaining_alerts = [
            s for s in sections if s.strip() and not s.startswith("<!--")
        ]

        if remaining_alerts:
            # Keep header + remaining alerts
            alert_path.write_text(
                _FILE_HEADER + "\n\n".join(remaining_alerts) + "\n",
                encoding="utf-8",
            )
        else:
            # No alerts left — empty the file
            alert_path.write_text("", encoding="utf-8")

        logger.info(
            "scheduler.alert_cleared",
            job_id=job_id,
            working_directory=str(working_directory),
            remaining_alerts=len(remaining_alerts),
        )
        return True

    except Exception:
        logger.exception(
            "scheduler.alert_clear_failed",
            job_id=job_id,
            working_directory=str(working_directory),
        )
        return False
